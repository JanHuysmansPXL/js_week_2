// Declareren = Variabelen aanmaken met een naam

let legoblokjes;

// initialiseren = waarde geven

legoblokjes = "10 blokjes";

// Declareren en Intitialiseren in 1 regel ( Let Variabele = Value)

let naam = "kimberly"; //declareren
let uitkomst;

// Je maakt een variabele, dan waarde, dan berekenen, dan output.

uitkomst = naam + " heeft " + legoblokjes;
console.log(uitkomst);

// demonstratie variabele met aanpassing blokjes en herberekening (uitkomst=..)

legoblokjes = "20 blokjes";
uitkomst = naam + " heeft " + legoblokjes; //berekening, verwerking
console.log(uitkomst); // output

